<p style="text-align: justify;" class="MsoNormal"><span>Активно формируется сеть сбыта: 9 дилеров представляют BOYARD в Новосибирске, Курске, Воронеже, Ижевске, Волжске, Казани, Краснодаре, Нижнем Новгороде. К концу 2003 года BOYARD сотрудничает более чем с тремя тысячами российских производителей корпусной мебели.</span></p>
 
<p style="margin-bottom: 6pt; text-align: justify;" class="MsoNormal"><b><span>2004.</span></b><span> ООО &laquo;Боярд&raquo; становится действующим членом Ассоциации Предприятий Мебельной и Деревообрабатывающей Промышленности России. Компания активно участвует в профессиональной жизни отрасли, в том числе в ведущих выставках. 
