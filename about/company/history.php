<p style="margin-bottom: 6pt; text-align: justify;" class="MsoNormal"><b><span> 2003. </span></b><span>Компания продала почти 12,5 миллионов петель, обеспечив, таким образом, качественными и надежными петлями порядка 3 миллионов гарнитурных модулей.</span></p>
 
<p style="text-align: justify;" class="MsoNormal"><span>Торговая марка BOYARD официально зарегистрирована в Российской Федерации. Стартовал процесс международной регистрации бренда.</span></p>
 
<p style="margin-bottom: 6pt; text-align: justify;" class="MsoNormal"><span>Запущено новое направление деятельности &mdash; лицевая фурнитура. Впервые на российском рынке по цене пластиковой появляется декоративная фурнитура из металла, в разнообразных цветовых решениях. Уже за первый год продаж было реализовано свыше 3 миллионов ручек.</span></p>
 
<p style="margin-bottom: 6pt; text-align: justify;" class="MsoNormal"><span>В 2003 году открыт филиал BOYARD в Москве &mdash; собственный офис со складскими помещениями.</span></p>
 
