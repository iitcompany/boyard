<p style="text-align: justify;" class="MsoNormal"><b><span>2005.</span></b><span> BOYARD завершил формирование полной линейки мебельных петель. В ассортименте &mdash; петли с различными углами открывания, петли Slide-on и </span><span lang="EN-US">C</span><span>lip-on с различными присадочными размерами, для разных материалов (стекла, алюминиевого профиля). По итогам 2005 года объем продаж систем мебельных петель вырос на 150 %.</span></p>
 
